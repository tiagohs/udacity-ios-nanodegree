//: ## If Statements
//: Some functions to execute under different conditions
//: Example 1
var hungry = false

if hungry {
    print("go eat")
}
//: Example 2
var wantTeddyBear = false
var haveMoney = true
if wantTeddyBear && haveMoney {
    print("buy teddy")
}
//: Example 3
var raining = true
if raining {
    print("watch a movie")
}
